public class ch18_9 {
	public static void main(String[] args) {
		Double x = Double.valueOf(10);					// Double類別
		Float y = Float.valueOf("101");					// Float類別
		System.out.println(x);
		System.out.println(y);
		System.out.println(x.getClass());				// 列出傳回值x類別
		System.out.println(y.getClass());				// 列出傳回值y類別
	}
}

