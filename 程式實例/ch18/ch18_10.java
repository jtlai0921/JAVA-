public class ch18_10 {
	public static void main(String[] args) {
		Integer x = new Integer(1000);							// Integer類別
		Double y = new Double(22.3456);							// Double類別
		System.out.println("intValue(x)    : " + x.intValue());
		System.out.println("byteValue(x)   : " + x.byteValue());
		System.out.println("doubleValue(x) : " + x.doubleValue());
		System.out.println("intValue(y)    : " + y.intValue());
		System.out.println("byteValue(y)   : " + y.byteValue());
		System.out.println("doubleValue(y) : " + y.doubleValue());
	}
}

