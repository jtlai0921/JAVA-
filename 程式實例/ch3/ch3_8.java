public class ch3_8 {
	public static void main(String[] args) {
		int x;
		long y;
		x = 103;						// 	]w10i炀慵
		System.out.println("CL103  涵 = " + x);
		x = 0b111;						// 	]w2i炀慵				
		System.out.println("CL0b111涵 = " + x);
		y = 022;						// 	]w8i炀慵
		System.out.println("CL022  涵 =  " + y);
		y = 0x2B;						// 	]w16i炀慵
		System.out.println("CL0x2B 涵 =  " + y);
	}
}

