public class ch10_9 {
	public static void main(String[] args) {
		double x = -3.49;						// 定義double
		System.out.println("-3.49的ceil() 值是 : " + Math.ceil(x));
		System.out.println("-3.49的floor()值是 : " + Math.floor(x));
		double y = 5.5;							// 定義double
		System.out.println("5.5  的ceil() 值是 : " + Math.ceil(y));
		System.out.println("5.5  的floor()值是 : " + Math.floor(y));
	}
}

