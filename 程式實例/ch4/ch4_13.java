public class ch4_13 {
	public static void main(String[] args) {
		int a, b = 5;
		a = 10;
		a += b;
		System.out.println("x = " + a);
		a = 10;
		a -= b;
		System.out.println("x = " + a);
		a = 10;
		a *= b;
		System.out.println("x = " + a);
		a = 10;
		a /= b;
		System.out.println("x = " + a);
		a = 10;
		a %= b;
		System.out.println("x = " + a);
	}
}

